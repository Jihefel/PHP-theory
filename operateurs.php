<?php
/* Opérateurs arithmétiques */

// Addition
$sum = 5 + 3; // $sum will be 8

// Soustraction
$difference = 10 - 4; // $difference will be 6

// Multiplication
$product = 2 * 6; // $product will be 12

// Division
$quotient = 20 / 5; // $quotient will be 4

// Modulo
$remainder = 15 % 4; // $remainder will be 3

// Exponentielle
$power = 2 ** 3; // $power will be 8


/* Opérateurs combinés en php */

// Addition assignment
$variable += 5; // Equivalent to: $variable = $variable + 5

// Subtraction assignment
$variable -= 3; // Equivalent to: $variable = $variable - 3

// Multiplication assignment
$variable *= 2; // Equivalent to: $variable = $variable * 2

// Division assignment
$variable /= 4; // Equivalent to: $variable = $variable / 4

// Modulo assignment
$variable %= 7; // Equivalent to: $variable = $variable % 7

// Concatenation assignment
$variable .= "world"; // Equivalent to: $variable = $variable . "world"
